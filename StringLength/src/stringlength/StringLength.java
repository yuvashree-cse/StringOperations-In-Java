public class StringLength{
    public static void main(String[] args){
        
        String sentence1 = "Hi! I am Yuvashree!";
        System.out.println("1st Sentence: "+sentence1);
        
        int length1 = sentence1.length();
        System.out.println("Length:" +length1);
        
        String sentence2 = "I can speak Tamil, English and Japanese!";
        System.out.println("2nd Sentence: "+sentence2);
        
        int length2 = sentence2.length();
        System.out.println("Length: "+length2);
    }
}